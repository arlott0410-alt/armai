export function deriveLocaleFromMerchant(
  input: { default_country?: string | null } | null | undefined
): Locale {
  const cc = input?.default_country?.toUpperCase().trim()
  if (cc === 'LA') return 'lo'
  if (cc === 'TH') return 'th'
  return 'en'
}
